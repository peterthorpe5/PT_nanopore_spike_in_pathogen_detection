#!/usr/bin/env python3
"""
Build method-performance summary tables from target-centric and reported-taxa
long tables.

This wrapper preserves the current target-centric benchmarking workflow and
adds support for a second reported-taxa long table so that genuine off-target
and cross-hit species can be summarised without overloading the original
`combined_long.tsv` logic.

Inputs
------
- combined_long.tsv
  The existing target-centric long table used for method benchmarking.
- reported_taxa_long.tsv
  An optional taxon-centric long table produced by the updated spike-in
  summariser. When provided, this is used to build the verbose
  "All reported species / off-target species" table.

Outputs
-------
All original outputs are retained, and two additional outputs are written when
reported taxa are available:

- reported_offtarget_species_summary.tsv
- reported_taxa_detection_calls.tsv

The HTML page and fragment also include the reported/off-target table.
"""

from __future__ import annotations

import argparse
import html
import importlib.util
import math
import sys
from pathlib import Path
from typing import Optional

import pandas as pd


def load_base_module():
    """
    Load the original performance-table module from the same directory.

    Returns
    -------
    module
        Imported base module.
    """
    this_path = Path(__file__).resolve()
    base_path = this_path.with_name("build_method_performance_table_with_offtargets.py")
    if not base_path.exists():
        raise FileNotFoundError(
            f"Required base script not found next to wrapper: {base_path}"
        )

    spec = importlib.util.spec_from_file_location(
        "build_method_performance_table_with_offtargets_base",
        str(base_path),
    )
    if spec is None or spec.loader is None:
        raise ImportError(f"Could not load module spec for {base_path}")

    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


BASE = load_base_module()


def parse_args() -> argparse.Namespace:
    """
    Parse command-line arguments.

    Returns
    -------
    argparse.Namespace
        Parsed arguments.
    """
    parser = argparse.ArgumentParser(
        description=(
            "Build method-level performance summaries from combined_long.tsv "
            "and, optionally, reported_taxa_long.tsv."
        )
    )
    parser.add_argument("--combined_long_tsv", required=True)
    parser.add_argument(
        "--reported_taxa_long_tsv",
        required=False,
        default=None,
        help=(
            "Optional reported_taxa_long.tsv generated by the updated spike-in "
            "summariser. When provided, this drives the reported/off-target "
            "species table."
        ),
    )
    parser.add_argument("--out_dir", required=True)
    parser.add_argument(
        "--threshold_mode",
        choices=["fixed", "baseline_mean", "baseline_max", "baseline_mean_plus_sd"],
        default="baseline_max",
    )
    parser.add_argument("--min_detect_value", type=float, default=1.0)
    parser.add_argument("--sd_multiplier", type=float, default=2.0)
    return parser.parse_args()


def normalise_reported_taxa_long(dataframe: pd.DataFrame) -> pd.DataFrame:
    """
    Normalise the reported-taxa long table.

    Parameters
    ----------
    dataframe : pd.DataFrame
        Raw reported-taxa long table.

    Returns
    -------
    pd.DataFrame
        Normalised reported-taxa long table.
    """
    required = {
        "workflow_type",
        "run_name",
        "taxon_name",
        "metric_name",
        "metric_value",
    }
    missing = required - set(dataframe.columns)
    if missing:
        raise ValueError(
            "reported_taxa_long.tsv is missing required columns: "
            + ", ".join(sorted(missing))
        )

    out_df = dataframe.copy()

    rename_map = {}
    if "workflow_type" in out_df.columns:
        rename_map["workflow_type"] = "workflow"
    if rename_map:
        out_df = out_df.rename(columns=rename_map)

    if "classifier" not in out_df.columns:
        out_df["classifier"] = "unknown_classifier"
    if "report_tsv" not in out_df.columns:
        out_df["report_tsv"] = pd.NA
    if "filter_prefix" not in out_df.columns:
        out_df["filter_prefix"] = pd.NA
    if "matched_target_labels" not in out_df.columns:
        out_df["matched_target_labels"] = ""
    if "replicate" not in out_df.columns:
        out_df["replicate"] = pd.NA
    if "spike_n_per_genome" not in out_df.columns:
        out_df["spike_n_per_genome"] = pd.NA
    if "total_spike_n" not in out_df.columns:
        out_df["total_spike_n"] = out_df["spike_n_per_genome"]
    if "n_genomes" not in out_df.columns:
        out_df["n_genomes"] = pd.NA
    if "is_shuffled_control" not in out_df.columns:
        out_df["is_shuffled_control"] = False

    out_df["workflow"] = out_df["workflow"].fillna("unknown").astype(str)
    out_df["run_name"] = out_df["run_name"].fillna("unknown_run").astype(str)
    out_df["classifier"] = out_df["classifier"].fillna("unknown_classifier").astype(str)
    out_df["taxon_name"] = out_df["taxon_name"].fillna("unknown_taxon").astype(str)
    out_df["metric_name"] = out_df["metric_name"].fillna("unknown_metric").astype(str)
    out_df["matched_target_labels"] = (
        out_df["matched_target_labels"].fillna("").astype(str)
    )

    numeric_columns = [
        "metric_value",
        "replicate",
        "spike_n_per_genome",
        "total_spike_n",
        "n_genomes",
        "percent",
    ]
    for column in numeric_columns:
        if column in out_df.columns:
            out_df[column] = pd.to_numeric(out_df[column], errors="coerce")

    out_df["is_shuffled_control"] = (
        out_df["is_shuffled_control"]
        .fillna(False)
        .astype(str)
        .str.lower()
        .isin(["true", "1", "yes"])
    )

    if "total_spike_n" in out_df.columns:
        out_df["spike_n"] = pd.to_numeric(out_df["total_spike_n"], errors="coerce")
    else:
        out_df["spike_n"] = pd.to_numeric(
            out_df["spike_n_per_genome"], errors="coerce"
        )

    out_df["is_positive"] = (~out_df["is_shuffled_control"]) & (out_df["spike_n"] > 0)
    out_df["is_negative"] = out_df["is_shuffled_control"] | (out_df["spike_n"] == 0)

    return out_df


def choose_reported_taxa_metric(dataframe: pd.DataFrame) -> pd.DataFrame:
    """
    Restrict to the primary count metric used for taxa-level thresholding.

    Parameters
    ----------
    dataframe : pd.DataFrame
        Normalised reported-taxa long table.

    Returns
    -------
    pd.DataFrame
        Subset restricted to reported clade reads.
    """
    metric_mask = dataframe["metric_name"].astype(str).str.endswith("_reported_clade_reads")
    subset = dataframe.loc[metric_mask].copy()
    if subset.empty:
        subset = dataframe.copy()
    return subset


def compute_reported_taxa_detection_calls(
    reported_taxa_df: pd.DataFrame,
    threshold_mode: str,
    min_detect_value: float,
    sd_multiplier: float,
) -> tuple[pd.DataFrame, pd.DataFrame]:
    """
    Compute taxa-level detection calls from the reported-taxa long table.

    Parameters
    ----------
    reported_taxa_df : pd.DataFrame
        Reported-taxa long table.
    threshold_mode : str
        Threshold rule.
    min_detect_value : float
        Minimum detection threshold.
    sd_multiplier : float
        Standard-deviation multiplier for baseline_mean_plus_sd mode.

    Returns
    -------
    tuple[pd.DataFrame, pd.DataFrame]
        Detection call table and threshold table.
    """
    if reported_taxa_df.empty:
        return pd.DataFrame(), pd.DataFrame()

    detection_rows: list[pd.DataFrame] = []
    threshold_rows: list[dict[str, object]] = []

    grouped = reported_taxa_df.groupby(
        ["workflow", "classifier", "taxon_name", "metric_name"],
        dropna=False,
    )

    for (workflow, classifier, taxon_name, metric_name), group in grouped:
        negatives = group.loc[group["is_negative"], "metric_value"]
        threshold_value = BASE.choose_threshold(
            negative_values=negatives,
            threshold_mode=threshold_mode,
            min_detect_value=min_detect_value,
            sd_multiplier=sd_multiplier,
        )
        group = group.copy()
        group["threshold_value"] = threshold_value
        group["detected"] = group["metric_value"] >= threshold_value
        detection_rows.append(group)
        threshold_rows.append(
            {
                "workflow": workflow,
                "classifier": classifier,
                "taxon_name": taxon_name,
                "metric_name": metric_name,
                "threshold_mode": threshold_mode,
                "threshold_value": threshold_value,
                "negative_mean": float(negatives.mean())
                if not negatives.dropna().empty
                else math.nan,
                "negative_max": float(negatives.max())
                if not negatives.dropna().empty
                else math.nan,
                "negative_sd": float(negatives.std(ddof=0))
                if not negatives.dropna().empty
                else math.nan,
                "n_negative_for_threshold": int(negatives.dropna().shape[0]),
            }
        )

    detection_df = (
        pd.concat(detection_rows, ignore_index=True)
        if detection_rows
        else pd.DataFrame()
    )
    threshold_df = pd.DataFrame(threshold_rows)
    return detection_df, threshold_df


def expected_species_map_from_combined_long(
    combined_long_df: pd.DataFrame,
) -> dict[tuple[str, str], list[str]]:
    """
    Build a map of expected species per workflow/run from the target-centric
    long table.

    Parameters
    ----------
    combined_long_df : pd.DataFrame
        Normalised combined long table.

    Returns
    -------
    dict[tuple[str, str], list[str]]
        Mapping from (workflow, run_name) to sorted expected species list.
    """
    if combined_long_df.empty:
        return {}

    positive_df = combined_long_df.loc[combined_long_df["is_positive"]].copy()
    if positive_df.empty:
        return {}

    mapping: dict[tuple[str, str], list[str]] = {}
    grouped = positive_df.groupby(["workflow", "run_name"], dropna=False)
    for (workflow, run_name), group in grouped:
        species = sorted(pd.unique(group["target_label"].astype(str)))
        mapping[(str(workflow), str(run_name))] = species
    return mapping


def build_reported_species_summary_from_taxa(
    reported_detection_df: pd.DataFrame,
    reported_threshold_df: pd.DataFrame,
    expected_species_map: dict[tuple[str, str], list[str]],
) -> pd.DataFrame:
    """
    Build a verbose run-level table of all reported species and off-target hits
    using the genuine reported-taxa long table.

    Parameters
    ----------
    reported_detection_df : pd.DataFrame
        Taxa-level detection call table.
    reported_threshold_df : pd.DataFrame
        Taxa-level threshold table.
    expected_species_map : dict[tuple[str, str], list[str]]
        Expected species per workflow/run derived from combined_long.tsv.

    Returns
    -------
    pd.DataFrame
        Verbose reported/off-target species summary.
    """
    if reported_detection_df.empty:
        return pd.DataFrame()

    rows: list[dict[str, object]] = []
    grouped = reported_detection_df.groupby(
        ["workflow", "classifier", "run_name"],
        dropna=False,
    )

    for (workflow, classifier, run_name), run_df in grouped:
        expected_species = expected_species_map.get(
            (str(workflow), str(run_name)),
            [],
        )

        all_species = sorted(pd.unique(run_df["taxon_name"].astype(str)))

        reported_any = sorted(
            pd.unique(
                run_df.loc[run_df["detected"] == True, "taxon_name"].astype(str)
            )
        )
        reported_positive = sorted(
            pd.unique(
                run_df.loc[
                    (run_df["is_positive"]) & (run_df["detected"] == True),
                    "taxon_name",
                ].astype(str)
            )
        )
        reported_negative = sorted(
            pd.unique(
                run_df.loc[
                    (run_df["is_negative"]) & (run_df["detected"] == True),
                    "taxon_name",
                ].astype(str)
            )
        )

        off_target_any = [x for x in reported_any if x not in expected_species]
        off_target_positive = [x for x in reported_positive if x not in expected_species]
        off_target_negative = [x for x in reported_negative if x not in expected_species]
        missing_expected = [x for x in expected_species if x not in reported_positive]

        threshold_rows = reported_threshold_df.loc[
            (reported_threshold_df["workflow"] == workflow)
            & (reported_threshold_df["classifier"] == classifier)
        ].copy()
        threshold_values = pd.to_numeric(
            threshold_rows["threshold_value"], errors="coerce"
        ).dropna()
        min_threshold = (
            float(threshold_values.min()) if not threshold_values.empty else math.nan
        )

        species_detail: list[str] = []
        for species in all_species:
            species_df = run_df.loc[run_df["taxon_name"] == species].copy()
            n_observations = int(species_df.shape[0])
            n_detected = int((species_df["detected"] == True).sum())
            pos_detected = int(
                ((species_df["is_positive"]) & (species_df["detected"] == True)).sum()
            )
            neg_detected = int(
                ((species_df["is_negative"]) & (species_df["detected"] == True)).sum()
            )
            detection_rate = BASE.safe_divide(n_detected, n_observations)
            metric_name = (
                str(species_df["metric_name"].dropna().iloc[0])
                if not species_df["metric_name"].dropna().empty
                else "unknown_metric"
            )
            threshold_row = reported_threshold_df.loc[
                (reported_threshold_df["workflow"] == workflow)
                & (reported_threshold_df["classifier"] == classifier)
                & (reported_threshold_df["taxon_name"] == species)
                & (reported_threshold_df["metric_name"] == metric_name)
            ]
            threshold_value = (
                float(threshold_row.iloc[0]["threshold_value"])
                if not threshold_row.empty
                else math.nan
            )
            species_detail.append(
                f"{species}: detected={n_detected}/{n_observations}, "
                f"positive={pos_detected}, negative={neg_detected}, "
                f"threshold={BASE.format_value(threshold_value)}, "
                f"rate={BASE.format_value(detection_rate)}"
            )

        if reported_any and not off_target_any and not missing_expected:
            interpretation = "Only expected tracked species reported"
        elif reported_any and off_target_any:
            interpretation = "Off-target species also reported"
        elif not reported_any:
            interpretation = "No species reported above threshold"
        else:
            interpretation = "Partial expected-species recovery"

        rows.append(
            {
                "Workflow": workflow,
                "Classifier": classifier,
                "Run": run_name,
                "Expected species": "; ".join(expected_species),
                "All reported species": "; ".join(reported_any),
                "Reported species in positives": "; ".join(reported_positive),
                "Reported species in negatives": "; ".join(reported_negative),
                "Off-target species": "; ".join(off_target_any),
                "Off-target species in positives": "; ".join(off_target_positive),
                "Off-target species in negatives": "; ".join(off_target_negative),
                "Missing expected species": "; ".join(missing_expected),
                "N expected species": len(expected_species),
                "N reported species": len(reported_any),
                "N off-target species": len(off_target_any),
                "Detection metric": "reported_clade_reads",
                "Min threshold": min_threshold,
                "Interpretation": interpretation,
                "Reported species detail": " | ".join(species_detail),
                "Note": (
                    "This table is derived from reported_taxa_long.tsv and so "
                    "retains genuine reported taxa rather than only tracked "
                    "target labels."
                ),
            }
        )

    reported_df = pd.DataFrame(rows)
    if reported_df.empty:
        return reported_df

    reported_df = reported_df.sort_values(
        by=["N off-target species", "N reported species", "Workflow", "Classifier", "Run"],
        ascending=[False, False, True, True, True],
    ).reset_index(drop=True)
    reported_df.insert(0, "Rank", range(1, len(reported_df) + 1))
    return reported_df


