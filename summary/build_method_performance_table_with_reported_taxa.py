def main() -> None:
    """
    Run the workflow.
    """
    args = parse_args()

    out_dir = Path(args.out_dir).expanduser().resolve()
    out_dir.mkdir(parents=True, exist_ok=True)

    combined_long = pd.read_csv(args.combined_long_tsv, sep="\t")
    combined_long = BASE.normalise_combined_long(combined_long)

    detection_df, threshold_df = BASE.compute_detection_calls(
        dataframe=combined_long,
        threshold_mode=args.threshold_mode,
        min_detect_value=args.min_detect_value,
        sd_multiplier=args.sd_multiplier,
    )

    method_df = BASE.summarise_method_performance(detection_df, threshold_df)
    compact_df = BASE.build_compact_table(method_df)
    panel_species_df = BASE.build_panel_species_summary(detection_df, threshold_df)
    by_spike_df = BASE.summarise_detection_by_spike(detection_df)
    definitions_df = pd.DataFrame(BASE.METRIC_DEFINITIONS)
    plot_explanations_df = pd.DataFrame(BASE.REPORT_PLOT_EXPLANATIONS)

    reported_species_df = pd.DataFrame()
    reported_detection_df = pd.DataFrame()
    if args.reported_taxa_long_tsv:
        reported_taxa_path = Path(args.reported_taxa_long_tsv).expanduser().resolve()
        if reported_taxa_path.exists() and reported_taxa_path.stat().st_size > 0:
            reported_taxa_long = pd.read_csv(reported_taxa_path, sep="\t")
            reported_taxa_long = normalise_reported_taxa_long(reported_taxa_long)
