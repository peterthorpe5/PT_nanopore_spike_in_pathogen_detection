
#!/usr/bin/env python3
"""
Build a combined spike-in benchmarking report with both tracked-target metrics and
stricter taxonomic-clean metrics that incorporate off-target species burden.

This script is intended to sit alongside the current spike-in summary outputs and
produce a single combined HTML report plus a formatted Excel workbook. It reads
the existing benchmark workbooks generated by the current pipeline and adds a
second set of "real-world" metrics that are closer to how a biologist would
experience the taxonomic output in practice.

Key ideas
---------
The existing tracked-target summaries ask whether the pre-specified target for a
workflow crossed threshold. They do not penalise a workflow for also reporting
multiple additional Plasmodium taxa. This script therefore reports both:

1. Tracked-target metrics
   Reused from the current summary workbooks.

2. Taxonomic-clean metrics
   Derived from the retained reported-taxa calls. These treat a positive
   observation as a clean recovery only if all expected panel species are
   recovered and no off-target taxa are reported above threshold. Negative
   observations are treated as taxonomically clean only if no taxa are reported
   above threshold.

Outputs
-------
- combined_real_world_report.html
- combined_real_world_report.xlsx
- multiple TSV support tables

The Excel workbook is formatted in the same general style as the earlier report
workbooks: dark blue headers, bold white header text, wrapped cells, filters,
and frozen top rows.

Notes
-----
- The script is intentionally conservative when constructing the taxonomic-clean
  metrics.
- For single-target runs represented as "single_target", any detected expected
  taxon counts as recovery of the expected panel.
- For mixed panels, all expected target labels must be recovered for a clean
  panel-level positive call.
"""

