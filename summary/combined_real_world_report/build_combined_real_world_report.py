def round_numeric_columns(dataframe: pd.DataFrame) -> pd.DataFrame:
    """
    Round floating-point columns for cleaner report output.

    Parameters
    ----------
    dataframe : pd.DataFrame
        Input DataFrame.

    Returns
    -------
    pd.DataFrame
        Rounded DataFrame.
    """
    out_df = dataframe.copy()
    for column in out_df.columns:
        if pd.api.types.is_float_dtype(out_df[column]):
            out_df[column] = out_df[column].round(3)
    return out_df


